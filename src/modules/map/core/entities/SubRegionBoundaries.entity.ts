export function isPointInPolygon(point: BoundaryPoint, polygon: BoundaryPoint[]): boolean {
  let inside = false
  for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
    const xi = polygon[i].x, yi = polygon[i].y
    const xj = polygon[j].x, yj = polygon[j].y
    const intersect = ((yi > point.y) !== (yj > point.y)) &&
      (point.x < (xj - xi) * (point.y - yi) / (yj - yi) + xi)
    if (intersect) inside = !inside
  }
  return inside
}

export function detectSubRegion(point: BoundaryPoint): { regionId: string, subRegionId: string } | null {
  for (const boundary of SUB_REGION_BOUNDARIES) {
    if (isPointInPolygon(point, boundary.polygon)) {
      return { regionId: boundary.regionId, subRegionId: boundary.subRegionId }
    }
  }
  return null
}
